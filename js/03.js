//tipos de datos
//undefine
let carlos;
console.log(typeof carlos);

//null
let nulo = null;
console.log(typeof nulo);

//tipado dinamico
//str
const string = "carlos";
console.log(typeof string);

//number
const number = 10;
let numer2 = 5;
console.log(number + numer2);

//concatenar difernetes tipos
let notNumber = "10";
console.log(number + notNumber);

//bigint
const bigInt = 10n;
console.log(typeof bigInt);

//boolean
const boolean = true;
console.log(typeof boolean);

//symbol
const symbol = Symbol(30);
const symbol2 = Symbol(30);
console.log(symbol === symbol2);

//object
const obj = {
  nombre: "carlos",
  edad: 20,
};
console.log(typeof obj);

